public class Variable {
    public static void main(String[] args) {
        byte coreQuantity = 1;
        short cost = 30_000;
        int starQuantity = 1_000_000_000;
        long microbeQuantity = 3_000_000_000l;
        float pi = 3.14f;
        double exactPi = 3.14159d;
        char firstLetter = 'A';
        boolean isWorking = false;
        
        System.out.println(coreQuantity);
        System.out.println(cost);
        System.out.println(starQuantity);
        System.out.println(microbeQuantity);
        System.out.println(pi);
        System.out.println(exactPi);
        System.out.println(firstLetter);
        System.out.println(isWorking);
    }
}